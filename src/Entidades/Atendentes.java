package Entidades;


public class Atendentes
{
  private int id;
  
  private String nome;
  private String pais;
  
  public Atendentes() {}
  
  public int getId()
  {
    return id;
  }
  
  public void setId(int id) {
    this.id = id;
  }
  
  public String getNome() {
    return nome;
  }
  
  public void setNome(String nome) {
    this.nome = nome;
  }
  


  public String getPais()
  {
    return pais;
  }
  
  public void setPais(String pais) {
    this.pais = pais;
  }
}
